// Escreva uma função em Go que receba um ponteiro para uma variável float64
// e atualize o valor da variável para a média aritmética
// entre o seu valor atual e o valor da constante pi
package main

func media(p *float64) {

	var x float64
	soma := x + 3.1415
	arit := soma / 2
	*p = arit
}
